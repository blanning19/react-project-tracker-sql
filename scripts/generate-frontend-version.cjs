#!/usr/bin/env node

const { execSync } = require('child_process');
const { readFileSync, writeFileSync } = require('fs');
const { join } = require('path');

function readPackageVersion() {
    try {
        const packageJsonPath = join(__dirname, '..', 'frontend', 'package.json');
        const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf-8'));
        return packageJson.version || '0.0.0';
    } catch (error) {
        return '0.0.0';
    }
}

function runGitCommand(command) {
    return execSync(command, {
        encoding: 'utf-8',
        stdio: ['pipe', 'pipe', 'ignore'],
    }).trim();
}

function getFrontendVersion() {
    try {
        return runGitCommand('git describe --tags --abbrev=0');
    } catch (error) {
        try {
            return `dev-${runGitCommand('git rev-parse --short HEAD')}`;
        } catch (fallbackError) {
            return readPackageVersion();
        }
    }
}

function getBuildInfo(version) {
    try {
        return {
            version,
            branch: runGitCommand('git rev-parse --abbrev-ref HEAD'),
            commitHash: runGitCommand('git rev-parse --short HEAD'),
            commitDate: runGitCommand('git log -1 --format=%cd --date=iso'),
            buildDate: new Date().toISOString(),
        };
    } catch (error) {
        return {
            version,
            branch: 'unknown',
            commitHash: 'unknown',
            commitDate: 'unknown',
            buildDate: new Date().toISOString(),
        };
    }
}

const version = getFrontendVersion();
const buildInfo = getBuildInfo(version);
const outputPath = join(__dirname, '..', 'frontend', 'src', 'shared', 'config', 'version.ts');

const content = `// Auto-generated by scripts/generate-frontend-version.cjs.
// Run "npm --prefix frontend run version:generate" to refresh.

export const APP_VERSION = '${version}';

export const APP_BUILD_INFO = {
    version: '${buildInfo.version}',
    branch: '${buildInfo.branch}',
    commitHash: '${buildInfo.commitHash}',
    commitDate: '${buildInfo.commitDate}',
    buildDate: '${buildInfo.buildDate}',
} as const;
`;

writeFileSync(outputPath, content, 'utf-8');

process.stdout.write(`Generated frontend version metadata: ${version}\n`);
